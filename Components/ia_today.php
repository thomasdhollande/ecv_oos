                    <h4>Analyse prédictive :</h4>
                    <p>
                        Les données des patients collectées sur les dossiers médicaux et celles obtenues à partir des objets connectés donnent au médecin l’accès à des informations précieuses sur le patient ainsi que sur la cohorte de population à laquelle le patient appartient.
                        <br>
                        Le calcul de ces données à l’aide d’algorithmes d’IA permet de développer le profil du patient et de construire des modèles prédictifs pour anticiper, diagnostiquer et traiter efficacement les maladies.
                    </p>

                    <h4>Pronostic :</h4>
