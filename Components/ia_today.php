                    <h4>Pharmacogénomique :</h4>
                    <p>
                        Les interactions et l’efficacité des médicaments varient d’une personne à l’autre et sont influencées par les variations génétiques. La pharmacogénomique vise à comprendre l’effet de ces variations sur les réponses individuelles aux médicaments. L’utilisation de l’IA pour passer au crible la grande quantité de données produites par les études génomiques peut aider à donner des indications utiles sur les mécanismes d’administration des médicaments.
                        <br>
                        En pharmacovigilance, la start-up bordelaise Synapse Medicine met à disposition sa technologie « Medication Shield », basée sur l’intelligence artificielle (IA) afin de faciliter la gestion des déclarations des effets indésirables lors de la campagne de vaccination Covid-19″
                    </p>

                    <h4>Découverte de médicaments et analyse des combinaisons de médicaments :</h4>
                    <p>
                        La recherche sur les médicaments prend de nombreuses années et coûte très cher avant que les essais cliniques et la commercialisation du médicament puissent avoir lieu. L’utilisation de l’IA pour rationaliser les processus de découverte et de réorientation des médicaments a le potentiel de stimuler considérablement le développement de nouveaux médicaments, de réduire le temps de mise sur le marché et de réduire également leurs coûts.
                        <br>
                        À titre d’exemple, BioSymetrics’, grâce à sa technologie d’IA s’attaque à la découverte de médicaments, de l’analyse clinique à l’expérimentation. L’intégration de millions de données et d’une technologie d’intelligence permet artificielle de générer des pistes plus efficaces à grande échelle.
