                    <h4>Chirurgie assistée par robot : </h4>
                    <p>
                        Les robots chirurgicaux utilisent l’IA pour utiliser les informations des opérations précédentes afin d’améliorer les techniques chirurgicales. Les données des dossiers préopératoires sont intégrées aux mesures opérationnelles pour améliorer les résultats de l’opération. Ces opérations sont peu invasives et la précision de l’instrument assisté par robot permet de réduire le degré du traumatisme post-op.
                    </p>

                    <h4>Chirurgies robotisées autonomes :</h4>
                    <p>
                        Bien qu’elles soient actuellement limitées à la science-fiction, les opérations robotisées peuvent devenir une réalité à l’avenir. L’utilisation du machine learning pour combiner la reconnaissance des formes motrices et l’interprétation visuelle des données peut permettre d’étendre la dextérité du chirurgien aux robots et de faire de la chirurgie robotique autonome une réalité. La chirurgie robotisée se limite actuellement à la commande à distance des robots par le chirurgien via un ordinateur, mais cela pourrait se transformer dans le futur.
                    </p>

                    <h4>Robots auxiliaires :</h4>
                    <p>
                        Ce sont des robots qui trouvent une application dans divers domaines, notamment les soins aux patients, les soins infirmiers, et les soins aux personnes âgées et aux patients affaiblis.
                    </p>

                    <h4>Chatbots :</h4>
                    <p>
                        Les chatbots sont des algorithmes alimentés par l’IA capables d’effectuer des conversations avec les patients. Ils ont le potentiel de devenir le premier point de contact pour les soins de santé primaires. La gravité de la requête est déterminée et les chatbots peuvent soit résoudre le problème, soit le transmettre au médecin. L’utilisation généralisée des chatbots réduit considérablement la charge de travail du médecin et évite d’avoir à se rendre inutilement chez les professionnels de santé. C’est le cas de l’entreprise Calmedica qui utilise l’intelligence artificielle pour dialoguer par SMS avec les patients avant et après une consultation et améliorer leur suivi.
