            <p>
                Dans tous les domaines, la crainte que cette technicité prenne le pas sur l’humanité
                perdure. Il est donc nécessaire d’en définir le cadre juridique et éthique.
            </p>
            <p>
                En matière de santé, l’IA ne doit pas altérer la relation entre le patient et le soignant, le
                lien humain est irremplaçable. Elle doit rester une aide pour le médecin, mais ne doit pas se
                substituer à celui-ci. Il s’agit également pour le corps médical de s’adapter et d’apprendre à
                utiliser ces nouvelles technologies, qui ont pour objectif d’épauler l’humain, pas de le
                remplacer
            </p>
            <p>
                Une autre difficulté que pose le développement de l’IA en médecine : la responsabilité
                du soignant. Actuellement, l’algorithme n’ayant pas de personnalité juridique, la
                responsabilité en cas d’erreur relève du droit de la responsabilité civile habituelle.
            </p>
            <p>
                Cependant, à l’avenir, l’autonomie décisionnelle dont seront capables ces systèmes
                pousse à s’interroger sur la nécessité d’adapter les régimes juridiques existants pour s’adapter
                aux enjeux de l’IA.
            </p>
            <p>
                Enfin, l’IA soulève la question de l’exploitation des données. Rappelons que pour
                fonctionner ces programmes nécessitent un accès à de nombreuses données. En Europe, la
                réglementation RGPD visant à protéger les données personnelles des individus ne définit pas
                de cadre précis pour l’IA. L’utilisation des ces données par l’IA doit ainsi suivre un cadre de
                régulation éthique, tout en évitant la sur-réglementation qui pourrait freiner l’innovation.
            </p>
